document.body.addEventListener("keydown", e => {
  switch (e.key.toLowerCase()) {
      case ('w'):
          break;

      case ('s'):
          break;

      case ('a'):
          break;

      case ('d'):
          break;
  }
})

document.body.addEventListener("keyup", e => {
  switch (e.key.toLowerCase()) {
      case ('w'):
          break;

      case ('s'):
          break;

      case ('a'):
          break;

      case ('d'):
          break;
  }
})